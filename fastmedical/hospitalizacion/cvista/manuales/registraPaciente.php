<div  id ="divManual" style="width:99%;height:99%;margin-left:1%;margin-right:1%;overflow:scroll;">
 Para registrar a un nuevo paciente se debe ingresar a la opción de Admisión/Registrar Datos del Paciente.
<br><br>
    <div align="center"><img src="../../../imagen/ayuda/imgRegPac1.png" alt="" width="65%"></div>
<br><br>
 Luego pulsar el botón "Nuevo" que se encuentra en la parte inferior izquierda.
<br><br>
    <div align="center"><img src="../../../imagen/ayuda/imgRegPac2.png" alt="" width="15%"></div>
<br><br>
Con esto se habilitarán los campos del lado derecho, los cuales deben ser completados.
<br><br>
    <div align="center"><img src="../../../imagen/ayuda/imgRegPac3.png" alt="" width="90%"></div>
<br><br>
Para el caso de la fecha se debera ingresar manualmente cuyo formato es (dia / mes / año) por ejemplo 28/12/2010. Luego automáticamente se calculará la edad del paciente como se muestra en la siguiente imagen.
<br><br>
    <div align="center"><img src="../../../imagen/ayuda/imgRegPac4.png" alt="" width="100%"></div>
<br><br>
En el caso de la fotografía al presionar el botón "Adjuntar fotografía" se abrirá una ventana para seleccionar la ruta donde está la imagen.
<br><br>
    <div align="center"><img src="../../../imagen/ayuda/imgRegPac5.png" alt="" width="65%"></div>
<br><br>
Luego de escribir los datos, se pulsa el botón "Guardar".
<br><br>
    <div align="center"><img src="../../../imagen/ayuda/imgRegPac6.png" alt="" width="90%"></div>
<br><br>
Por último se acepta la confirmación del guardado.
<br><br>
    <div align="center"><img src="../../../imagen/ayuda/imgRegPac7.png" alt="" width="50%"></div>
</div>

  